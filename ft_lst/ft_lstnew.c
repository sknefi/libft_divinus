#include "../libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_linked_list;

	new_linked_list = (t_list *)malloc(sizeof(t_list));
	if (!new_linked_list)
		return (NULL);
	new_linked_list->content = content;
	new_linked_list->next = NULL;
	return (new_linked_list);
}
